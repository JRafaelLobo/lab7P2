    public String GenerarAleatorio(int numero) {
        String temp = "";
        for (int i = 0; i < numero; i++) {
            int letra_numero = (int) (Math.random() * 1);
            if (letra_numero == 1) {
                //letra minusculas
                temp += (char) ((int) (Math.random() * 25) + 97);
            } else {
                //numero
                temp += (char) ((int) (Math.random() * 9) + 48);
            }
        }
        return temp;
    }

